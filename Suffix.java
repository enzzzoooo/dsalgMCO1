public class Suffix {

    private String stringDNA;
    private int index;

    public Suffix(String stringDNA, int index) {
        this.stringDNA = stringDNA;
        this.index = index;
    }

    public String getStringDNA() {
        return stringDNA;
    }
    public void setStringDNA(String stringDNA) {
        this.stringDNA = stringDNA;
    }
    public int getIndex() {
        return index;
    }
    public void setIndex(int index) {
        this.index = index;
    }

}